from math import pi, log

m = float(input('Введите массу яйца от 47 до овердохера граммов: '))
C = 3.7
Pd=1.038
K=5.4E-3
T0 = 4
Tw = 100
Ty = 70

A = (m**(2.0/3)*C*(Pd**(1.0/3)))/(K*(pi**2)*((4*pi/3)**(2.0/3)))
LOG=log(0.76*(T0 - Tw)/(Ty - Tw))
t=A*LOG
print(u'Время варки вашего яйца всмятку в кипящей воде %d c.' %t)
